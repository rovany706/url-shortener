func main() {
	// staticcheck
	var staticChecks []*analysis.Analyzer
	for _, v := range staticcheck.Analyzers {
		// Add SA class
		if strings.HasPrefix(v.Analyzer.Name, "SA") {
			staticChecks = append(staticChecks, v.Analyzer)
		}

		// Add some other checks
		// S1030 - Use bytes.Buffer.String or bytes.Buffer.Bytes
		// S1011 - Use a single append to concatenate two slices
		// QF1003 - Convert if/else-if chain to tagged switch
		switch v.Analyzer.Name {
		case "S1030", "ST1011", "QF1003":
			staticChecks = append(staticChecks, v.Analyzer)
		}
	}

	// std checks
	stdAnalyzers := []*analysis.Analyzer{
		appends.Analyzer,
		asmdecl.Analyzer,
		assign.Analyzer,
		atomic.Analyzer,
		atomicalign.Analyzer,
		bools.Analyzer,
		buildssa.Analyzer,
		buildtag.Analyzer,
		cgocall.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		deepequalerrors.Analyzer,
		defers.Analyzer,
		directive.Analyzer,
		errorsas.Analyzer,
		findcall.Analyzer,
		framepointer.Analyzer,
		httpmux.Analyzer,
		httpresponse.Analyzer,
		ifaceassert.Analyzer,
		inspect.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilfunc.Analyzer,
		nilness.Analyzer,
		printf.Analyzer,
		reflectvaluecompare.Analyzer,
		shadow.Analyzer,
		shift.Analyzer,
		sigchanyzer.Analyzer,
		slog.Analyzer,
		sortslice.Analyzer,
		stdmethods.Analyzer,
		stdversion.Analyzer,
		stringintconv.Analyzer,
		structtag.Analyzer,
		testinggoroutine.Analyzer,
		tests.Analyzer,
		timeformat.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
		unusedwrite.Analyzer,
		usesgenerics.Analyzer,
		waitgroup.Analyzer,
	}

	// Accept Interfaces, Return Concrete Types
	ireturnAnalyzer := ireturn.NewAnalyzer()
	// Errcheck is a program for checking for unchecked errors in Go code. These unchecked errors can be critical bugs in some cases.
	errCheckAnalyzer := errcheck.Analyzer

	analyzers := append(staticChecks, stdAnalyzers...)
	analyzers = append(analyzers, errCheckAnalyzer, ireturnAnalyzer)

	// osexitcheck
	analyzers = append(analyzers, myAnalyzers.OsExitAnalyzer)

	multichecker.Main(
		analyzers...,
	)
}
